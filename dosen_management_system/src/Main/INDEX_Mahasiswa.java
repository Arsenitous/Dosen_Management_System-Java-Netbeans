   public void loadData() throws SQLException {
    MahasiswaDAO mahasiswaDAO = new MahasiswaDAO();
    List<Mahasiswa> data = mahasiswaDAO.getData(); 
    String[][] list = new String[data.size()][11];
    SimpleDateFormat dateFormat = new SimpleDateFormat("d MMMM yyyy");

    for (int i = 0; i < data.size(); i++) {
        list[i][0] = String.valueOf(data.get(i).getId());
        list[i][1] = String.valueOf(data.get(i).getNik());
        list[i][2] = data.get(i).getNama();
        list[i][3] = data.get(i).getTempat_lahir();
        
        Date tanggalLahir = data.get(i).getTanggal();
        list[i][4] = tanggalLahir != null ? dateFormat.format(tanggalLahir) : "-";
        
        list[i][5] = data.get(i).getEmail();
        list[i][6] = data.get(i).getJenis_kelamin();
        list[i][7] = String.valueOf(data.get(i).getIps());
        list[i][8] = String.valueOf(data.get(i).getIpk());
        list[i][9] = data.get(i).getJurusan();
        list[i][10] = data.get(i).getAngkatan();
    }

    String[] columnName = {
        "ID", "NIK", "Nama", "Tempat Lahir", "Tanggal Lahir", 
        "Email", "Jenis Kelamin", "IPS", "IPK", "Jurusan", "Angkatan"
    };

    table_mahasiswa.setModel(new javax.swing.table.DefaultTableModel(list, columnName));
}

    
