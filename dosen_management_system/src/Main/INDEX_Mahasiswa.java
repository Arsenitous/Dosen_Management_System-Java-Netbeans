    private void btn_delMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_delMouseClicked
    if(selectedId != -1) {
        int confirmation = JOptionPane.showConfirmDialog(this, "Apakah ingin dihapus?", "Peringatan",
                JOptionPane.YES_OPTION);
        if (confirmation == JOptionPane.YES_OPTION){
            MahasiswaDAO mahasiswaDAO;
            try {
            mahasiswaDAO = new MahasiswaDAO();
            mahasiswaDAO.deleteMahasiswa(selectedId);
            JOptionPane.showMessageDialog(this, "Berhasil", "Suksess", JOptionPane.INFORMATION_MESSAGE);
            loadData();
            selectedId = -1;
            } catch (SQLException e){
                    JOptionPane.showMessageDialog(this, "Error", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    }//GEN-LAST:event_btn_delMouseClicked

    private void btn_updMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_updMouseClicked
       if(selectedId != -1){
         try {
             MahasiswaDAO mahasiswaDAO = new MahasiswaDAO();
             Mahasiswa mahasiswa = mahasiswaDAO.getMahasiswaById(selectedId);
             if(mahasiswa != null){
                 UPDATE_Mahasiswa upd = new UPDATE_Mahasiswa(this, mahasiswa);
                 upd.setVisible(true);
                 upd.setLocationRelativeTo(null);
             }
         } catch (SQLException e){
                    JOptionPane.showMessageDialog(this, "Error", "Error", 
                    JOptionPane.ERROR_MESSAGE);
     }}
    }//GEN-LAST:event_btn_updMouseClicked

