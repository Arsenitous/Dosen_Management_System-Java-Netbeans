    private void btn_updMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_updMouseClicked
   String nama = txt_nama.getText();
    String tempat = txt_tempat.getText();
    Date tanggal = jdate_tanggal.getDate();
    String email = txt_email.getText();
    String jenis = radiobtn_laki.isSelected() ? "Laki-Laki" : "Perempuan";
    int nip = Integer.parseInt(txt_nip.getText());
    int nidn = Integer.parseInt(txt_nidn.getText());
    Date tanggal_masuk = jdate_tanggal_masuk.getDate();
    Date tanggal_selesai = jdate_tanggal_selesai.getDate();
    
     if (nama.trim().isEmpty() || tempat.trim().isEmpty() || tanggal == null || email.trim().isEmpty()
            || (!radiobtn_laki.isSelected() && !radiobtn_perempuan.isSelected())
            || txt_nip.getText().trim().isEmpty() || txt_nidn.getText().trim().isEmpty() || tanggal_masuk == null) {
     JOptionPane.showMessageDialog(this, "Harap isi semua data!", "Peringatan", 
     JOptionPane.ERROR_MESSAGE);
     } else {
        Dosen d = new Dosen (0, nama, tempat, tanggal, email, jenis, nip, nidn, tanggal_masuk, tanggal_selesai);
        try {
      DosenDAO dosenDAO = new DosenDAO();
      int res = dosenDAO.updateDosen(d, dt.getId());
      if (res == 1) {
      JOptionPane.showMessageDialog(this, "Create Data Suksess", "Suksess", 
      JOptionPane.INFORMATION_MESSAGE); 
        }  index.loadData();
     } catch (SQLException ex){
        JOptionPane.showMessageDialog(this, "Ada Error Saat Insert Data", "Error", 
        JOptionPane.ERROR_MESSAGE); } this.dispose(); }
    
    }//GEN-LAST:event_btn_updMouseClicked

