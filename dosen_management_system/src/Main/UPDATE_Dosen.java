    public void loadDataUpdate() {
    txt_nama.setText(dt.getNama());                       
    txt_tempat.setText(dt.getTempat_lahir());             
    if (dt.getTanggal() != null) {                        
        jdate_tanggal.setDate(dt.getTanggal());
    } else {
        jdate_tanggal.setDate(null);
    }
    txt_email.setText(dt.getEmail());                   

    // Jenis kelamin
    if ("Laki-Laki".equalsIgnoreCase(dt.getJenis_kelamin())) {
        radiobtn_laki.setSelected(true);
    } else if ("Perempuan".equalsIgnoreCase(dt.getJenis_kelamin())) {
        radiobtn_perempuan.setSelected(true);
    }

    txt_nip.setText(String.valueOf(dt.getNip()));          
    txt_nidn.setText(String.valueOf(dt.getNidn()));       

    if (dt.getTanggal_mulai() != null) {                 
        jdate_tanggal_masuk.setDate(dt.getTanggal_mulai());
    } else {
        jdate_tanggal_masuk.setDate(null);
    }

    if (dt.getTanggal_selesai() != null) {               
        jdate_tanggal_selesai.setDate(dt.getTanggal_selesai());
    } else {
        jdate_tanggal_selesai.setDate(null);
    }
}
    
